    <h1>Par mums</h1>
    <p>Latvijas skolēnam ir vairākas iespējas pārbaudīt savas fizikas zināšanas individuāli, piemēram, Valsts Fizikas olimpiādē vai Atklātajā fizikas olimpiādē. Olimpiādes mērķis ir popularizēt fiziku skolēnu vidū, attīstīt prasmes strādāt komandā, kā arī sagatavot skolēnus valsts un starptautiska līmeņa olimpiādēm fizikā, iepazīstinot ar idejām, tehnikām un uzdevumiem, kas var parādīties šādās olimpiādēs.</p>
    <p>Olimipādi organizē skolēnu un absolventu komanda no Rīgas Valsts 1. ģimnāzijas. 2020./2021. gadā to sastāda Rolands Lopatko, Jānis Pudāns, Kims Georgs Pavlovs, Ilmārs Štolcers, Vilhelms Cinis, Daniels Gorovojs, Olivers Prānis, Ričards Kristers Knipšis, Oskars Mednis, Gustavs Švalbe un citi.</p>
    <p>Izsakām pateicību arī Atai Krūmiņai, Pēterim Bricam un citiem Rīgas Valsts 1. ģimnāzijas skolotājiem.</p>
